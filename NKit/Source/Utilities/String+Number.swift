import Foundation

public extension Int {
    public var nk_string: String {
        return "\(self)"
    }
}

public extension Double {
    public var nk_string: String {
        return "\(self)"
    }
}

public extension Float {
    public var nk_string: String {
        return "\(self)"
    }
}
